// Lightweight, dependency-free internationalization for the app UI. The app ships two interface
// languages — English and 日本語 — chosen by a persisted setting (see settingsStore `locale`). This
// module is PURE (no store/React imports) so it stays trivially testable; the React binding lives in
// `useI18n.ts`.
//
// Two lookup modes, by design:
//   • translate(key, locale)  — follows the user's language setting (the default for ~all UI chrome).
//   • translateBoth(key)      — ALWAYS renders both languages, for terms where showing both helps every
//                               reader regardless of setting (Japanese typographic terms a Western
//                               letterer still needs to recognise: 右綴じ / ルビ / 圏点 …). Set on the
//                               message via `both`, or auto-joined "en · ja".

export type AppLocale = 'en' | 'ja';

