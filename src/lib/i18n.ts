export type MessageKey = keyof typeof MESSAGES;

/** Translate a key into the given locale (falls back to the raw key if it is somehow missing). */
export function translate(key: MessageKey, locale: AppLocale): string {
  // `MESSAGES` is `as const` (so `keyof` yields the key union) — widen the looked-up value back to
  // the interface so the optional `both` field stays visible. `| undefined` keeps the runtime
  // fallback honest for a key that isn't in the catalog.
  const message = MESSAGES[key] as I18nMessage | undefined;
  if (!message) return key;
  // Guard against an undefined/invalid `locale` (e.g. a server snapshot before the store is
  // hydrated): always return a real string by falling back to English.
  return message[locale] ?? message.en;
}

/**
 * Translate a key, then substitute `{name}`-style placeholders. Interpolation (rather than string
 * concatenation) is required because word order differs between languages — e.g. `Add {name} node`
 * vs `{name}ノードを追加`.
 */
export function translateFormat(
  key: MessageKey,
  locale: AppLocale,
  params: Record<string, string | number>,
): string {
  let out = translate(key, locale);
  for (const [name, value] of Object.entries(params)) {
    out = out.split(`{${name}}`).join(String(value));
  }
  return out;
}

/** Render a key in BOTH languages (locale-independent) — for terms that should always show both. */
export function translateBoth(key: MessageKey): string {
  const message = MESSAGES[key] as I18nMessage | undefined;
  if (!message) return key;
  if (message.both) return message.both;
  return message.en === message.ja ? message.en : `${message.en} · ${message.ja}`;
}

/** The initial locale for a fresh install: Japanese when the environment prefers it, else English. */
export function resolveDefaultLocale(): AppLocale {
  if (typeof navigator === 'undefined') return 'en';
  const langs = [navigator.language, ...(navigator.languages ?? [])];
  return langs.some((lang) => lang?.toLowerCase().startsWith('ja')) ? 'ja' : 'en';
}

/** Narrow an arbitrary persisted value back to a valid locale (defaulting when it is stale/absent). */
export function normalizeLocale(value: unknown): AppLocale {
  return value === 'ja' || value === 'en' ? value : resolveDefaultLocale();
}
